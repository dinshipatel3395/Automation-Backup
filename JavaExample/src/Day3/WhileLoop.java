/*
 In this program first it will check the condition as its entry control loop.
 It will print the value of i till it becomes 15. 
 */


package Day3;

public class WhileLoop 
{
	public static void main(String[] args)
	{
		int i=10;
		while(i<=15)
			
		{
			System.out.println("Ans:-  "+i);
			i++;
		}
	}
}
