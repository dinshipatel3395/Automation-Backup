public class EqualToString 
{
	static String a="Dinshi";
	static String b="Dinshi";
	static int c=30;
	static int d=20;
	static char e='A';
	static char f='B';
	public static void main(String args[]) 
	{
		System.out.println("Equal To="+(a==b));
		System.out.println("Equal To="+(c==d));
		System.out.println("Equal To="+(e==f));
		System.out.println("Equal To="+(a.equals(b)));
	}
}



